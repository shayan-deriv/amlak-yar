    /**
     * Run the database seeds.
     *
     * @return void
     */
    public function run()
    {
        $this->createStatesItems($this->states);
    }

    public function createStatesItems($states)
    {
        foreach ($states as $state) {
            $createdState = State::updateOrCreate([
                'fa_name' => $state['fa_name']], [
                'en_name' => $state['en_name'],
            ]);

            if ( isset($state['cities']) ) {
                foreach ( $state['cities'] as $cityId => $city ) {
                    City::updateOrCreate([
                        'id' => $cityId
                    ], [
                        'state_id' => $createdState->id,
                        'name' => $city
                    ]);
                }
            }
        }
    }
